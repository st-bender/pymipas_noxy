def _ap_da():
    np.random.seed(1234)
    ts = pd.date_range("2000-01-01", "2005-01-01") + pd.to_timedelta(12, unit="h")
    # roughly mimic Ap distribution
    app = xr.DataArray(
        np.exp(2 + 1 * np.random.randn(ts.shape[0])),
        dims=["time"],
        coords={"time": ts},
    )
    return app


